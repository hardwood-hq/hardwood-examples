package dev.hardwood.examples.layermodel;

import java.io.IOException;
import java.io.InputStream;
import java.io.UncheckedIOException;
import java.nio.ByteBuffer;

import dev.hardwood.InputFile;

/// Provides the small nested-schema sample files this example reads.
///
/// These are tiny, hand-built Parquet fixtures bundled on the classpath (`src/main/resources`),
/// not downloaded from the network. Because they are only a couple of kilobytes each, we read them
/// straight into memory and open them with [InputFile#of(ByteBuffer)] — no local copy on disk.
///
/// They are Hardwood's own test fixtures, generated by its PyArrow data generator
/// [`tools/simple-datagen.py`](https://github.com/hardwood-hq/hardwood/blob/v1.0.0.CR2/tools/simple-datagen.py)
/// and checked into the Hardwood source tree under `core/src/test/resources` at the `v1.0.0.CR2`
/// tag. Both use the modern, three-level Parquet encoding for nested types (each linked to its
/// source file):
///   - [`address_book_test.parquet`](https://github.com/hardwood-hq/hardwood/blob/v1.0.0.CR2/core/src/test/resources/address_book_test.parquet):
///     a required `owner`, a `list<string>` of phone numbers, and a
///     `list<struct<name, phoneNumber>>` of contacts.
///   - [`map_types_test.parquet`](https://github.com/hardwood-hq/hardwood/blob/v1.0.0.CR2/core/src/test/resources/map_types_test.parquet):
///     `map<string,string>`, `map<int32,int64>`, and `map<string,bool>`.
public final class Datasets {

    public static final String ADDRESS_BOOK = "address_book_test.parquet";
    public static final String MAP_TYPES = "map_types_test.parquet";

    /// Reads a bundled sample file from the classpath into an in-memory [InputFile].
    public static InputFile sample(String name) {
        try (InputStream in = Datasets.class.getClassLoader().getResourceAsStream(name)) {
            if (in == null) {
                throw new IOException("Bundled sample not found on classpath: " + name);
            }
            return InputFile.of(ByteBuffer.wrap(in.readAllBytes()));
        } catch (IOException e) {
            throw new UncheckedIOException(e);
        }
    }
}
