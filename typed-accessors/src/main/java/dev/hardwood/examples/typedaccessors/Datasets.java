package dev.hardwood.examples.typedaccessors;

import java.io.IOException;
import java.io.InputStream;
import java.io.UncheckedIOException;
import java.nio.ByteBuffer;

import dev.hardwood.InputFile;

/// Provides the small logical-type sample files this example reads.
///
/// These are tiny, hand-built Parquet fixtures bundled on the classpath (`src/main/resources`),
/// not downloaded from the network. Because they are only a few kilobytes each, we read them
/// straight into memory and open them with [InputFile#of(ByteBuffer)] — no local copy on disk.
///
/// They are Hardwood's own test fixtures, generated by its PyArrow data generator
/// [`tools/simple-datagen.py`](https://github.com/hardwood-hq/hardwood/blob/v1.0.0.CR2/tools/simple-datagen.py)
/// and checked into the Hardwood source tree under `core/src/test/resources` at the `v1.0.0.CR2`
/// tag. Each isolates the logical types it demonstrates (each linked to its source file):
///   - [`logical_types_test.parquet`](https://github.com/hardwood-hq/hardwood/blob/v1.0.0.CR2/core/src/test/resources/logical_types_test.parquet):
///     DATE, TIME, TIMESTAMP (UTC), DECIMAL, UUID, JSON, and BSON.
///   - [`local_timestamp_test.parquet`](https://github.com/hardwood-hq/hardwood/blob/v1.0.0.CR2/core/src/test/resources/local_timestamp_test.parquet):
///     UTC vs. local (zone-free) TIMESTAMP columns side by side.
///   - [`interval_logical_type_test.parquet`](https://github.com/hardwood-hq/hardwood/blob/v1.0.0.CR2/core/src/test/resources/interval_logical_type_test.parquet):
///     the INTERVAL type (`months`/`days`/`milliseconds`).
///   - [`float16_logical_type_test.parquet`](https://github.com/hardwood-hq/hardwood/blob/v1.0.0.CR2/core/src/test/resources/float16_logical_type_test.parquet):
///     half-precision FLOAT16 widened to `float`.
///   - [`null_logical_type_test.parquet`](https://github.com/hardwood-hq/hardwood/blob/v1.0.0.CR2/core/src/test/resources/null_logical_type_test.parquet):
///     the NULL type — every value is absent by definition.
public final class Datasets {

    public static final String LOGICAL_TYPES = "logical_types_test.parquet";
    public static final String LOCAL_TIMESTAMP = "local_timestamp_test.parquet";
    public static final String INTERVAL = "interval_logical_type_test.parquet";
    public static final String FLOAT16 = "float16_logical_type_test.parquet";
    public static final String NULL_TYPE = "null_logical_type_test.parquet";

    /// Reads a bundled sample file from the classpath into an in-memory [InputFile].
    public static InputFile sample(String name) {
        try (InputStream in = Datasets.class.getClassLoader().getResourceAsStream(name)) {
            if (in == null) {
                throw new IOException("Bundled sample not found on classpath: " + name);
            }
            return InputFile.of(ByteBuffer.wrap(in.readAllBytes()));
        } catch (IOException e) {
            throw new UncheckedIOException("Failed to read bundled sample: " + name, e);
        }
    }
}
